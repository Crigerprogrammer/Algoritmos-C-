#include <iostream>

using namespace std;

int main()
{
    /**OPERADORES MATEMATICOS**/
    int numero1 = 10;
    int numero2 = 3;
    //Suma +
    int suma = numero1 + numero2;
    cout<<"Esta es una suma "<<suma<<endl;
    //Resta -
    int resta = numero1 - numero2;
    cout<<"Esta es una resta "<<resta<<endl;
    //Multiplicación *
    int multiplicacion = numero1 * numero2;
    cout<<"Esta es una multiplicacion "<<multiplicacion<<endl;
    //Disivión /
    int cociente = numero1/numero2;
    cout<<"El cociente de los dos numero es "<<cociente<<endl;
    // Modulo %
    int residuo = numero1%numero2;
    cout<<"El residuo de los dos numero es "<<residuo<<endl;

    /** DISIVION ENTERA **/

    cout<<"Esta es una division entera "<<7/3 << endl;
    cout<<"Esta es una division real "<<7.0/3<<endl;
    return 0;

}
