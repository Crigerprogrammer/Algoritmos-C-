#include <iostream>
//Aca se agraga la nueva libreria de C++ que dara mas operaciones aritmeticas
#include <cmath>
using namespace std;

int main()
{
    /**Potencia**/
    double potencia = pow(5,5);
    cout<<"La potencia es "<<potencia<<endl;
    /**Raiz Cuadrada**/
    double raiz = sqrt(100.0);
    cout<<"La raiz cuadrada es "<<raiz<<endl;
    /**Seno**/
    double seno = sin(0.523599);
    cout<<"El seno es "<<seno<<endl;
    /**Coseno**/
    double coseno = cos(1.0472);
    cout<<"El coseno es "<<coseno<<endl;
    /**Tangente**/
    double tangente = tan(0.785398);
    cout<<"La tangente es "<<tangente<<endl;
}
