#include <iostream>

using namespace std;

int main()
{
    //Valores Enteros
    int a = 10;
    int b = 20;
    int edad = a + b ;
    cout << edad << endl;

    //Valores Decimales o Reales (Floats o double)
    float c = 20.80;
    float d = 30.121;
    float resultado = c + d;
    cout << resultado << endl;

    //Valores Booleanos (bool)
    bool valor1 = true;
    bool valor2 = false;
    cout << valor1 << endl;
    cout << valor2 << endl;

    //Caracteres (char)
    char z = 'Hola Mundo';
    char x = ' Cristian Hernandez';
    cout<< z << endl;
    cout<< x << endl;

    //Notación cientifica
    int valorz = 3e3;
    double valory = 5.3e3;
    cout<< valorz << endl;
    cout << valory << endl;
}
